package laberintos;


public class posicion {
    private int X,Y;

    public posicion(int X, int Y) {
        this.X = X;
        this.Y = Y;
    }

    public int getX() {
        return X;
    }

    public void setX(int X) {
        this.X = X;
    }

    public int getY() {
        return Y;
    }

    public void setY(int Y) {
        this.Y = Y;
    }

    @Override
    public String toString() {
        return "posicion{" + "X=" + X + ", Y=" + Y + '}';
    }
    
    
}
